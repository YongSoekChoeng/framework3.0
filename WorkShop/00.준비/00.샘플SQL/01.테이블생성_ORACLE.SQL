
CREATE TABLE SAMPLE_GROUP (
  GROUP_ID VARCHAR2(30) NOT NULL, 
  NAME VARCHAR2(40) NOT NULL,    
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (GROUP_ID)
);

CREATE TABLE SAMPLE_MEMBER (
  GROUP_ID VARCHAR2(30) NOT NULL, 
  USER_ID VARCHAR2(30) NOT NULL,   
  DEPT_ID VARCHAR2(30) NULL,
  USER_PW VARCHAR2(30) NULL,   
  NAME VARCHAR2(40) NOT NULL,   
  AGE NUMBER(12) DEFAULT NULL,   
  DUTY VARCHAR2(40) DEFAULT NULL,     
  REGION VARCHAR2(100) DEFAULT NULL, 
  ADDRESS VARCHAR2(255) DEFAULT NULL, 
  ADDRESS_DTL VARCHAR2(255) DEFAULT NULL,  
  JUMINNO VARCHAR2(13) DEFAULT NULL,    
  GENDER VARCHAR2(2) DEFAULT NULL,    
  TEL VARCHAR2(15) DEFAULT NULL,    
  HP VARCHAR2(15) DEFAULT NULL,    
  EMAIL VARCHAR2(150) DEFAULT NULL,   
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (GROUP_ID, USER_ID)
);

CREATE TABLE SAMPLE_DEPT (
  DEPT_ID VARCHAR2(30) NOT NULL,   
  DEPT_NAME VARCHAR2(40) NOT NULL,   
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (DEPT_ID)
);
