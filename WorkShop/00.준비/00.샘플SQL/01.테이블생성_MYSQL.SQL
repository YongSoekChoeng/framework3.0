
CREATE TABLE SAMPLE_GROUP (
  GROUP_ID VARCHAR(30) NOT NULL, 
  NAME VARCHAR(40) NOT NULL,    
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (GROUP_ID)
);

CREATE TABLE SAMPLE_MEMBER (
  GROUP_ID VARCHAR(30) NOT NULL, 
  USER_ID VARCHAR(30) NOT NULL,   
  DEPT_ID VARCHAR(30) NULL,
  USER_PW VARCHAR(30) NULL,   
  NAME VARCHAR(40) NOT NULL,   
  AGE NUMERIC(12) DEFAULT NULL,   
  DUTY VARCHAR(40) DEFAULT NULL,     
  REGION VARCHAR(100) DEFAULT NULL, 
  ADDRESS VARCHAR(255) DEFAULT NULL, 
  ADDRESS_DTL VARCHAR(255) DEFAULT NULL,  
  JUMINNO VARCHAR(13) DEFAULT NULL,    
  GENDER VARCHAR(2) DEFAULT NULL,    
  TEL VARCHAR(15) DEFAULT NULL,    
  HP VARCHAR(15) DEFAULT NULL,    
  EMAIL VARCHAR(150) DEFAULT NULL,   
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (GROUP_ID, USER_ID)
);

CREATE TABLE SAMPLE_DEPT (
  DEPT_ID VARCHAR(30) NOT NULL,   
  DEPT_NAME VARCHAR(40) NOT NULL,   
  INPUT_DT DATE NOT NULL,  
  PRIMARY KEY  (DEPT_ID)
);
